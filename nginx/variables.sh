$host       	        # The host header from the request
$remote_addr	        # IP address of the client
$request_uri	        # Full original request URI
$uri	                # Normalized URI without arguments
$args	                # Arguments from the request (query string)
$request_method	        # GET, POST, etc.
$http_user_agent	    # User agent
$server_name	        # The server name of the request
$document_root	        # The root directory for the request
$request_filename	    # Full path to the file for the request
$query_string	        # Same as $args
